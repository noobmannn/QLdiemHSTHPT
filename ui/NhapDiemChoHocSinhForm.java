    static String[] createStringListData(List<HocSinh> datahs) {
        String[] mahs = new String[datahs.size()];
        for (int i = 0; i < datahs.size(); i++) {
            mahs[i] = datahs.get(i).getMaHS();
        }
        return mahs;
    }

